/**
  * @brief This function handles Hard fault interrupt.
  */
void HardFault_Handler(void)
{
  /* USER CODE BEGIN HardFault_IRQn 0 */
  WWDG->CR |= 0x7F;

  GATE_DIS();

  WWDG->CR |= 0x7F;

  energocycle_write_flash_C();

  WWDG->CR |= 0x7F;

#ifdef _UART_DEBUG
struct
{
	uint32_t r0;
	uint32_t r1;
	uint32_t r2;
	uint32_t r3;
	uint32_t r12;
	uint32_t lr;
	uint32_t pc;
	uint32_t psr;
}*stack_ptr; //Указатель на текущее значение стека(SP)

  WWDG->CR |= 0x7F;

asm(
	"TST lr, #4 \n" //Тестируем 3ий бит указателя стека(побитовое �?)
	"ITE EQ \n"   //Значение указателя стека имеет бит 3?
	"MRSEQ %[ptr], MSP  \n"  //Да, сохраняем основной указатель стека
	"MRSNE %[ptr], PSP  \n"  //Нет, сохраняем указатель стека процесса
	: [ptr] "=r" (stack_ptr)
);

	LOG("HardFault_Handler\n");
	WWDG->CR |= 0x7F;
	LOG("r0=%08X, r1=%08X, r2=%08X, r3=%08X, \n"
		"r12=%08X, lr=%08X, pc=%08X, psr=%08X, \n"
		"BFAR=%08X, CFSR=%08X, HFSR=%08X, DFSR=%08X, AFSR=%08X, SCB_SHCSR=%08X\n",
		stack_ptr->r0, stack_ptr->r1, stack_ptr->r2, stack_ptr->r3,
		stack_ptr->r12, stack_ptr->lr, stack_ptr->pc, stack_ptr->psr,
		(*((volatile unsigned long *)(0xE000ED38))),
		(*((volatile unsigned long *)(0xE000ED28))),
		(*((volatile unsigned long *)(0xE000ED2C))),
		(*((volatile unsigned long *)(0xE000ED30))),
		(*((volatile unsigned long *)(0xE000ED3C))),
		SCB->SHCSR);
#endif

  WWDG->CR |= 0x7F;

  HAL_NVIC_SystemReset();

  WWDG->CR |= 0x7F;
  /* USER CODE END HardFault_IRQn 0 */
  while (1)
  {
    /* USER CODE BEGIN W1_HardFault_IRQn 0 */
    /* USER CODE END W1_HardFault_IRQn 0 */
  }
}

